FROM PRIME_PROPERTIES_DW.LANDING.STG_PROPERTIES;

-- 3. Fact Table banana (Isme hum JOIN laga kar Commission calculate karenge)
CREATE OR REPLACE TABLE PRIME_PROPERTIES_DW.ANALYTICS.FACT_TRANSACTIONS AS
SELECT 
    T.TRANSACTION_ID,
    T.TRANSACTION_DATE,
    T.PROPERTY_ID,
    T.AGENT_ID,
    T.TRANSACTION_TYPE,
    T.AMOUNT,
    -- Calculation: Amount ko Agent ke rate se multiply karna
    (T.AMOUNT * A.COMMISSION_RATE) AS COMMISSION_EARNED 
FROM PRIME_PROPERTIES_DW.LANDING.STG_TRANSACTIONS T
JOIN PRIME_PROPERTIES_DW.LANDING.STG_AGENTS A ON T.AGENT_ID = A.AGENT_ID;
-- Check karein ke data Fact table mein calculations ke sath aaya ya nahi
