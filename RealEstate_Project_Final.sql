-- 1. Agent Performance Query
SELECT 
    A.AGENT_NAME, 
    SUM(F.AMOUNT) as TOTAL_REVENUE,
    COUNT(F.TRANSACTION_ID) as TOTAL_DEALS
FROM PRIME_PROPERTIES_DW.ANALYTICS.FACT_TRANSACTIONS F
JOIN PRIME_PROPERTIES_DW.ANALYTICS.DIM_AGENT A ON F.AGENT_ID = A.AGENT_ID
GROUP BY A.AGENT_NAME
ORDER BY TOTAL_REVENUE DESC;

